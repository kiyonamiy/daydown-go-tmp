package auth

// 认证（Authentication，简称 Authn）： 一般指身份验证，指通过一定的手段，完成对用户身份的确认。认证用来证明你是谁。

// 授权（Authorization，简称 Authz）： 授权发生在身份认证成功之后，用来确认你对某个资源是否有某类操作权限。授权用来证明你能做什么。
